export class GearDimensions {
    teethNumber: number;
    dedendumDepth: number;
    addendumHeight: number;
    toothThickness: number;
    addendumRadius: number;
    
    constructor(teethNumber: number, dedendumDepth: number, addendumHeight: number, toothThickness: number, addendumRadius: number){
        this.teethNumber = teethNumber;
        this.dedendumDepth = dedendumDepth;
        this.addendumHeight = addendumHeight;
        this.toothThickness = toothThickness;
        this .addendumRadius = addendumRadius;
    }
}